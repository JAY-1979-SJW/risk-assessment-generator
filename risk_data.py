def find_risks_for_work(work_description: str) -> list:
    """
    작업 내용에서 키워드를 찾아 해당하는 위험요소 목록 반환

    Args:
        work_description: 작업일보의 작업 내용 텍스트

    Returns:
        해당하는 위험요소 딕셔너리 리스트
    """
    found_risks = []
    matched_keys = set()

    for keyword, risk_key in KEYWORD_MAPPING.items():
        if keyword in work_description and risk_key not in matched_keys:
            matched_keys.add(risk_key)
            if risk_key in RISK_DATA:
                found_risks.extend(RISK_DATA[risk_key])

    return found_risks


def calculate_risk_level(possibility: int, severity: int) -> tuple:
    """
    위험성 수준 계산

    Returns:
        (위험성 점수, 위험성 등급)
    """
    risk_score = possibility * severity

    if risk_score <= 2:
        level = "낮음"
    elif risk_score <= 4:
        level = "보통"
    elif risk_score <= 6:
        level = "높음"
    else:
        level = "매우높음"

    return risk_score, level


